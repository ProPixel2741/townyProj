package WrappedTownObjects.WrapperSuperClasses;

public class WrapperMoney {
    private Integer money;

    public void addMoney(Integer addMoney) {
        money += addMoney;
    }

    public void subtractMoney(Integer subtractMoney) {
        money -= subtractMoney;
    }

    public void setMoney(Integer setMoney) {
        money = setMoney;
    }

}
